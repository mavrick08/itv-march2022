# This python file does the calculation like (add, mul,substract & div)


def add(num1,num2):
    res = num1 + num2
    return res


def mul(num1,num2):
    res = num1 * num2
    return res

def substract(num1,num2):
    res = num1 - num2
    return res

def div(num1,num2):
    res = num1 / num2
    return res